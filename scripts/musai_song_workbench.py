def now_stamp() -> str:
    return datetime.now().strftime("%Y%m%d-%H%M%S")


def iso_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def slugify(value: str) -> str:
    cleaned = re.sub(r"[^A-Za-z0-9._-]+", "-", value.strip().lower()).strip("-")
    return cleaned or "song"


def ensure_dir(path: Path) -> Path:
    path.mkdir(parents=True, exist_ok=True)
    return path


def read_text_arg(text: str | None, file_path: str | None) -> str:
    if file_path:
        return Path(file_path).expanduser().read_text(encoding="utf-8").strip()
    return (text or "").strip()


def write_json(path: Path, data: Any) -> None:
    path.write_text(json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")


def toml_string(value: str | Path) -> str:
    return json.dumps(str(value), ensure_ascii=False)


def language_label(code: str) -> tuple[str, str, str]:
    return LANG_LABELS.get(code, (code, code, ""))


def build_caption(args: argparse.Namespace) -> str:
    parts = [
        args.style or "cinematic polished pop song",
        args.genre or "",
        args.mood or "",
        f"{args.voice_notes}.",
        f"Vocal language: {args.vocal_language}.",
        f"Sing the provided lyrics as one continuous song with connected phrases and no clipped final words.",
        f"{args.keyscale}, {args.bpm} BPM, {args.time_signature}/4.",
    ]
    if args.idea:
        parts.insert(0, f"Song idea: {args.idea}.")
    if args.character:
        parts.insert(1, f"Character theme: {args.character}.")
    return " ".join(part.strip() for part in parts if part and part.strip())


def render_ace_config(
    project_dir: Path,
    lyrics_path: Path,
    language: str,
    caption: str,
    duration: int,
    bpm: int,
    keyscale: str,
    time_signature: str,
    model: str,
    seed: int,
    output_name: str,
    inference_steps: int,
    guidance_scale: float,
) -> str:
    save_dir = project_dir / "ace_outputs" / output_name
    lines = [
        "# Generated by Musai song workbench.",
        f"project_root = {toml_string(ACE_ROOT)}",
        f"checkpoint_dir = {toml_string(ACE_CHECKPOINTS)}",
        f"save_dir = {toml_string(save_dir)}",
        f"config_path = {toml_string(model)}",
        'task_type = "text2music"',
        f"caption = {toml_string(caption)}",
        f"lyrics = {toml_string(lyrics_path)}",
        f"duration = {int(duration)}",
        f"bpm = {int(bpm)}",
        f"keyscale = {toml_string(keyscale)}",
        f"timesignature = {toml_string(time_signature)}",
        f"vocal_language = {toml_string(language)}",
        "thinking = false",
        "use_cot_lyrics = false",
        "use_cot_caption = false",
        f"inference_steps = {int(inference_steps)}",
        f"guidance_scale = {float(guidance_scale)}",
        "use_random_seed = false",
        f"seeds = [{int(seed)}]",
        "batch_size = 1",
        'audio_format = "wav"',
        "",
    ]
    return "\n".join(lines)


