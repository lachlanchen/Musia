def render_commands(project_dir: Path, config_path: Path, spec: dict[str, Any]) -> str:
    language = spec["vocal_language"]
    project_dir = project_dir.resolve()
    config_path = config_path.resolve()
    lyrics_path = Path(spec["lyrics_path"]).resolve()
    session = f"musai-song-{slugify(spec['title'])[:32]}"
    return "\n".join(
        [
            "#!/usr/bin/env bash",
            "set -euo pipefail",
            f"ROOT_DIR={toml_string(ROOT)}",
            f"PROJECT_DIR={toml_string(project_dir)}",
            f"CONFIG={toml_string(config_path)}",
            f"LYRICS={toml_string(lyrics_path)}",
            f"LANGUAGE={toml_string(language)}",
            'cd "$ROOT_DIR"',
            "",
            'latest_audio() { find "$PROJECT_DIR/ace_outputs" -type f -name "*.wav" 2>/dev/null | sort | tail -n 1; }',
            "",
            'case "${1:-help}" in',
            "  generate)",
            '    cd third_party/ACE-Step-1.5',
            '    .venv/bin/python cli.py -c "$CONFIG" --backend "${ACE_BACKEND:-vllm}"',
            "    ;;",
            "  generate-tmux)",
            f"    tmux new-session -d -s {session} \"cd {ROOT} && {project_dir / 'commands.sh'} generate; read -p done\"",
            "    ;;",
            "  review)",
            '    audio="$(latest_audio)"',
            '    if [[ -z "${audio:-}" ]]; then echo "No generated WAV found." >&2; exit 1; fi',
            '    PYTHONNOUSERSITE=1 conda run -n musai python scripts/musai_song_workbench.py review --project-dir "$PROJECT_DIR" --audio "$audio" --lyrics-file "$LYRICS" --language "$LANGUAGE" --run-analysis',
            "    ;;",
            "  analyze)",
            '    audio="$(latest_audio)"',
            '    if [[ -z "${audio:-}" ]]; then echo "No generated WAV found." >&2; exit 1; fi',
            '    PYTHONNOUSERSITE=1 conda run -n musai python scripts/run_pipeline.py "$audio" --run-name "$(basename "$PROJECT_DIR")-analysis" --max-duration 180 --asr-model small --language "$LANGUAGE" --demucs-device "${MUSAI_DEMUCS_DEVICE:-cuda}"',
            "    ;;",
            "  correct)",
            '    shift || true',
            '    PYTHONNOUSERSITE=1 conda run -n musai python scripts/musai_song_workbench.py correct --project-dir "$PROJECT_DIR" "$@"',
            "    ;;",
            "  handoff)",
            '    audio="$(latest_audio)"',
            '    PYTHONNOUSERSITE=1 conda run -n musai python scripts/musai_song_workbench.py handoff --project-dir "$PROJECT_DIR" ${audio:+--audio "$audio"}',
            "    ;;",
            "  show)",
            '    sed -n "1,240p" "$PROJECT_DIR/PROMPT.md"',
            "    ;;",
            "  help|*)",
            "    echo 'Usage: commands.sh generate|generate-tmux|review|analyze|correct|handoff|show'",
            "    ;;",
            "esac",
            "",
        ]
    )


def find_latest_audio(project_dir: Path) -> Path | None:
    files = sorted((project_dir / "ace_outputs").glob("**/*.wav"))
    return files[-1] if files else None


def cmd_init(args: argparse.Namespace) -> None:
    lyrics = read_text_arg(args.lyrics, args.lyrics_file)
    if not lyrics:
        lyrics = DEFAULT_LYRICS.get(args.vocal_language, DEFAULT_LYRICS["en"]).strip()
    project_dir = Path(args.output_dir).expanduser() if args.output_dir else PROJECTS_ROOT / f"{now_stamp()}-{slugify(args.title)}"
    project_dir = ensure_dir(project_dir).resolve()
    lyrics_dir = ensure_dir(project_dir / "lyrics")
    lang = args.vocal_language
    lyrics_path = lyrics_dir / f"{lang}.txt"
    lyrics_path.write_text(lyrics.strip() + "\n", encoding="utf-8")

    caption = build_caption(args)
    config_path = project_dir / f"ace_{lang}.toml"
    spec = {
        "schema": "musai.song.workbench.v1",
        "created_at": iso_now(),
        "title": args.title,
        "idea": args.idea or "",
        "character": args.character or "",
        "language": args.language,
        "vocal_language": lang,
        "duration": args.duration,
        "bpm": args.bpm,
        "keyscale": args.keyscale,
        "time_signature": args.time_signature,
        "style": args.style or "",
        "genre": args.genre or "",
        "mood": args.mood or "",
        "voice_notes": args.voice_notes or "",
        "video_style": args.video_style or "",
        "model": args.model,
        "seed": args.seed,
        "inference_steps": args.inference_steps,
        "guidance_scale": args.guidance_scale,
        "caption": caption,
        "lyrics": lyrics.strip(),
        "lyrics_path": str(lyrics_path),
        "project_dir": str(project_dir),
        "ace_config": str(config_path),
    }
    config_path.write_text(
        render_ace_config(
            project_dir=project_dir,
            lyrics_path=lyrics_path,
            language=lang,
            caption=caption,
            duration=args.duration,
            bpm=args.bpm,
            keyscale=args.keyscale,
            time_signature=args.time_signature,
            model=args.model,
            seed=args.seed,
            output_name=lang,
            inference_steps=args.inference_steps,
            guidance_scale=args.guidance_scale,
        ),
        encoding="utf-8",
    )
    write_json(project_dir / "song_spec.json", spec)
    (project_dir / "PROMPT.md").write_text(render_prompt_md(spec), encoding="utf-8")
    (project_dir / "REVIEW_CHECKLIST.md").write_text(render_review_checklist(spec), encoding="utf-8")
    (project_dir / "LALACHAN_HANDOFF.md").write_text(render_lalachan_handoff(spec), encoding="utf-8")
    commands = project_dir / "commands.sh"
    commands.write_text(render_commands(project_dir, config_path, spec), encoding="utf-8")
    commands.chmod(0o755)
    print(project_dir)
    print(f"Config: {config_path}")
    print(f"Commands: {commands}")


def run_subprocess(cmd: list[str], cwd: Path = ROOT) -> tuple[int, str]:
    proc = subprocess.run(cmd, cwd=cwd, text=True, stdout=subprocess.PIPE, stderr=subprocess.STDOUT)
    return proc.returncode, proc.stdout


def cmd_review(args: argparse.Namespace) -> None:
    project_dir = Path(args.project_dir).expanduser().resolve()
    audio = Path(args.audio).expanduser().resolve()
    lyrics_file = Path(args.lyrics_file).expanduser().resolve() if args.lyrics_file else project_dir / "lyrics" / f"{args.language}.txt"
    review_dir = ensure_dir(project_dir / "reviews" / now_stamp())

    quality_cmd = [
        sys.executable,
        str(ROOT / "scripts" / "musai_quality_check.py"),
        str(audio),
        "--language",
        args.language,
        "--expected-lyrics-file",
        str(lyrics_file),
        "--output-dir",
        str(review_dir),
    ]
    if args.asr_model:
        quality_cmd.extend(["--asr-model", args.asr_model])
    quality_code, quality_log = run_subprocess(quality_cmd)

    analysis_dir = ""
    analysis_log = ""
    if args.run_analysis:
        run_name = f"{project_dir.name}-{review_dir.name}-analysis"
        analysis_cmd = [
            sys.executable,
            str(ROOT / "scripts" / "run_pipeline.py"),
            str(audio),
            "--run-name",
            run_name,
            "--max-duration",
            str(args.max_duration),
            "--asr-model",
            args.asr_model or "small",
            "--language",
            args.language,
            "--demucs-device",
            args.demucs_device,
        ]
        analysis_code, analysis_log = run_subprocess(analysis_cmd)
        if analysis_code == 0:
            analysis_dir = str(ROOT / "data" / "runs" / run_name)

    quality = {}
    quality_path = review_dir / "quality.json"
    if quality_path.exists():
        quality = json.loads(quality_path.read_text(encoding="utf-8"))

    notes = []
    gate = quality.get("quality_gate", "unknown")
    if gate != "pass":
        notes.append("Automated quality gate is not pass; use `commands.sh correct` for a new prompt/seed pass.")
    if quality.get("lyric_overlap") is not None and quality.get("lyric_overlap", 0) < 0.55:
        notes.append("Lyric overlap is low. Shorten or simplify lyrics, raise vocal clarity in caption, or regenerate with a new seed.")
    if quality.get("rms", 0) < 0.02:
        notes.append("RMS is low. Consider a stronger vocal-forward prompt or post-mix loudness correction.")
    if not notes:
        notes.append("Automated checks are acceptable; still do a human listening pass for melody, phrasing, and emotional quality.")

    report = [
        f"# Song Review - {project_dir.name}",
        "",
        f"- Audio: `{audio}`",
        f"- Lyrics: `{lyrics_file}`",
        f"- Language: `{args.language}`",
        f"- Quality gate: `{gate}`",
        f"- ASR text: `{quality.get('asr_text', '')}`",
        f"- Lyric overlap: `{quality.get('lyric_overlap')}`",
        f"- RMS / peak: `{quality.get('rms')}` / `{quality.get('peak')}`",
        f"- Analysis dir: `{analysis_dir or 'not run'}`",
        "",
        "## Review Notes",
        "",
        *[f"- {note}" for note in notes],
        "",
        "## Quality Command Log",
        "",
        "```text",
        quality_log[-4000:],
        "```",
    ]
    if analysis_log:
        report += ["", "## Analysis Command Log", "", "```text", analysis_log[-4000:], "```"]
    (review_dir / "SONG_REVIEW.md").write_text("\n".join(report) + "\n", encoding="utf-8")
    print(review_dir / "SONG_REVIEW.md")


def cmd_correct(args: argparse.Namespace) -> None:
    project_dir = Path(args.project_dir).expanduser().resolve()
    spec_path = project_dir / "song_spec.json"
    if not spec_path.exists():
        raise SystemExit(f"Missing {spec_path}")
    spec = json.loads(spec_path.read_text(encoding="utf-8"))
    stamp = now_stamp()
    correction_dir = ensure_dir(project_dir / "corrections" / stamp)
    lyrics = read_text_arg(args.lyrics, args.lyrics_file) or spec["lyrics"]
    lyrics_path = correction_dir / f"{spec['vocal_language']}.txt"
    lyrics_path.write_text(lyrics.strip() + "\n", encoding="utf-8")
    seed = args.seed if args.seed is not None else int(spec.get("seed", 1000)) + 17
    caption = spec["caption"]
    if args.issues:
        caption += f" Correction notes: {args.issues}."
    if args.caption_extra:
        caption += " " + args.caption_extra.strip()
    config_path = correction_dir / f"ace_{spec['vocal_language']}_corrected.toml"
    config_path.write_text(
        render_ace_config(
            project_dir=project_dir,
            lyrics_path=lyrics_path,
            language=spec["vocal_language"],
            caption=caption,
            duration=int(args.duration or spec["duration"]),
            bpm=int(args.bpm or spec["bpm"]),
            keyscale=args.keyscale or spec["keyscale"],
            time_signature=str(spec.get("time_signature", "4")),
            model=args.model or spec["model"],
            seed=seed,
            output_name=f"{spec['vocal_language']}_corrected_{stamp}",
            inference_steps=int(args.inference_steps or spec.get("inference_steps", 8)),
            guidance_scale=float(args.guidance_scale or spec.get("guidance_scale", 1.0)),
        ),
        encoding="utf-8",
    )
    plan = [
        f"# Correction Plan - {spec['title']}",
        "",
        f"- Issues: {args.issues or 'manual correction pass'}",
        f"- Corrected config: `{config_path}`",
        f"- Corrected lyrics: `{lyrics_path}`",
        f"- Seed: `{seed}`",
        "",
        "Run:",
        "",
        "```bash",
        f"cd {ACE_ROOT} && .venv/bin/python cli.py -c {config_path} --backend vllm",
        "```",
        "",
    ]
    (correction_dir / "CORRECTION_PLAN.md").write_text("\n".join(plan), encoding="utf-8")
    print(correction_dir / "CORRECTION_PLAN.md")


def cmd_handoff(args: argparse.Namespace) -> None:
    project_dir = Path(args.project_dir).expanduser().resolve()
    spec = json.loads((project_dir / "song_spec.json").read_text(encoding="utf-8"))
    audio = args.audio or str(find_latest_audio(project_dir) or "")
    cover = args.cover or ""
    out = Path(args.output).expanduser() if args.output else project_dir / "LALACHAN_SONG_TO_VIDEO_HANDOFF.md"
    out.write_text(render_lalachan_handoff(spec, audio_path=audio, cover_path=cover), encoding="utf-8")
    print(out)


def cmd_find_audio(args: argparse.Namespace) -> None:
    audio = find_latest_audio(Path(args.project_dir).expanduser().resolve())
    if not audio:
        raise SystemExit("No generated WAV found.")
    print(audio)


